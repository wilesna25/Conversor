    private void bntCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntCrearActionPerformed
        if (!lblRut_sta.getText().trim().equals("")) {
            try {
                txtNod_lat.setText(model.numeroNodos(txtLai.getText().replace(",", "."), txtLaf.getText().replace(",", "."), txtLain.getText().replace(",", ".")) + "");
                txtNod_lon.setText(model.numeroNodos(txtLoi.getText().replace(",", "."), txtLof.getText().replace(",", "."), txtLoin.getText().replace(",", ".")) + "");
                txtNod_pro.setText(model.numeroNodos(txtPri.getText().replace(",", "."), txtPrf.getText().replace(",", "."), txtPrin.getText().replace(",", ".")) + "");
                model.crearModel1p(lblRut_sta.getText().replace(",", "."), txtLoi.getText().replace(",", "."), txtLof.getText().replace(",", "."), txtLoin.getText().replace(",", "."), txtLai.getText().replace(",", "."), txtLaf.getText().replace(",", "."), txtLain.getText().replace(",", "."),
                        txtPri.getText().replace(",", "."), txtPrf.getText().replace(",", "."), txtPrin.getText().replace(",", "."));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "El archivo model1p no se pudo crear, verifique ingres칩 todos los par치metros de entrada");
                System.out.println("e = " + e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Porfavor cargue el archivo .sta que usar치 \n"
                    + " como referencia para la creaci칩n del archivo model1p");
        }
    }//GEN-LAST:event_bntCrearActionPerformed

    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("STA", "STA");
        chooser.setFileFilter(filter);
        int returnVal = chooser.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            String ruta_esta = chooser.getSelectedFile().getAbsolutePath();
            lblRut_sta.setText(ruta_esta);
        }
    }//GEN-LAST:event_btnCargarActionPerformed
